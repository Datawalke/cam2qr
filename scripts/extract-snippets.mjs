#!/usr/bin/env node
// Generates site/snippets.js from the `// #region snippet: <id>` anchors in src/.
// The "How it works" page shows these excerpts beside each pipeline stage; extracting
// them from the real source at generation time is what keeps the page from drifting
// when the code changes.
//
//   node scripts/extract-snippets.mjs            regenerate site/snippets.js
//   node scripts/extract-snippets.mjs --check    exit 1 when the committed file is stale
//
// Anchor grammar (marker lines are excluded from the excerpt):
//   // #region snippet: some-id
//   ...code...
//   // #endregion snippet
//
// test/unit/snippets.test.ts runs --check, so `pnpm test` fails on a stale file.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(fileURLToPath(new URL('..', import.meta.url)));
const srcDir = join(root, 'src');
const outFile = join(root, 'site', 'snippets.js');

const START = /^\s*\/\/ #region snippet: ([a-z0-9-]+)\s*$/;
const END = /^\s*\/\/ #endregion snippet\s*$/;

function* walkTsFiles(dir) {
  for (const entry of readdirSync(dir, { withFileTypes: true }).sort((a, b) =>
    a.name.localeCompare(b.name),
  )) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) yield* walkTsFiles(path);
    else if (entry.isFile() && entry.name.endsWith('.ts')) yield path;
  }
}

function dedent(lines) {
  let indent = Number.POSITIVE_INFINITY;
  for (const line of lines) {
    if (line.trim() === '') continue;
    indent = Math.min(indent, line.length - line.trimStart().length);
  }
  if (!Number.isFinite(indent) || indent === 0) return lines;
  return lines.map((line) => line.slice(indent));
}

function extractFile(path, snippets) {
  const file = relative(root, path).split('\\').join('/');
  const lines = readFileSync(path, 'utf8').split('\n');
  let open = null;
  for (let i = 0; i < lines.length; i++) {
    const start = lines[i].match(START);
    if (start) {
      if (open) throw new Error(`${file}:${i + 1}: nested snippet region`);
      open = { id: start[1], firstLine: i + 1 };
      continue;
    }
    if (END.test(lines[i])) {
      if (!open) throw new Error(`${file}:${i + 1}: #endregion without #region`);
      if (snippets.has(open.id)) throw new Error(`duplicate snippet id "${open.id}" in ${file}`);
      const body = dedent(lines.slice(open.firstLine, i));
      snippets.set(open.id, {
        file,
        // 1-based line of the first excerpt line, for GitHub deep links.
        line: open.firstLine + 1,
        code: body.join('\n').replace(/\s+$/, ''),
      });
      open = null;
    }
  }
  if (open) throw new Error(`${file}: unclosed snippet region "${open.id}"`);
}

function render(snippets) {
  const entries = [...snippets.entries()].sort(([a], [b]) => a.localeCompare(b));
  const body = entries
    .map(
      ([id, s]) =>
        `  '${id}': {\n    file: ${JSON.stringify(s.file)},\n    line: ${s.line},\n` +
        `    code: ${JSON.stringify(s.code)},\n  },`,
    )
    .join('\n');
  return (
    '/* Generated by scripts/extract-snippets.mjs from the `// #region snippet:` anchors\n' +
    ' * in src/. Do not edit by hand; regenerate with `pnpm site:snippets`. */\n' +
    `window.CAM2QR_SNIPPETS = {\n${body}\n};\n`
  );
}

const snippets = new Map();
for (const path of walkTsFiles(srcDir)) extractFile(path, snippets);
if (snippets.size === 0) throw new Error('no snippet anchors found under src/');
const rendered = render(snippets);

if (process.argv.includes('--check')) {
  let committed = null;
  try {
    committed = readFileSync(outFile, 'utf8');
  } catch {}
  if (committed !== rendered) {
    console.error(
      'site/snippets.js is out of date with the snippet anchors in src/.\n' +
        'Run `pnpm site:snippets` and commit the result.',
    );
    process.exit(1);
  }
  console.log(`site/snippets.js is current (${snippets.size} snippets).`);
} else {
  writeFileSync(outFile, rendered);
  console.log(`wrote site/snippets.js (${snippets.size} snippets: ${[...snippets.keys()].sort().join(', ')})`);
}
