import type { ErrorCorrectionLevel } from '../types.js';

export interface EcBlockGroup {
  /** Number of blocks with this shape. */
  count: number;
  /** Data codewords per block (EC codewords come on top). */
  dataCodewords: number;
}

export interface EcBlocks {
  ecCodewordsPerBlock: number;
  groups: EcBlockGroup[];
}

export interface VersionInfo {
  version: number;
  size: number;
  alignmentPositions: number[];
  ecBlocks: Record<ErrorCorrectionLevel, EcBlocks>;
}

export function versionForSize(size: number): number | null {
  if (size < 21 || size > 177 || (size - 17) % 4 !== 0) return null;
  return (size - 17) / 4;
}

export function sizeForVersion(version: number): number {
  return version * 4 + 17;
}

/** Total codewords (data + EC) in a version, derived from the EC table. */
export function totalCodewords(info: VersionInfo, level: ErrorCorrectionLevel): number {
  const { ecCodewordsPerBlock, groups } = info.ecBlocks[level];
  return groups.reduce((sum, g) => sum + g.count * (g.dataCodewords + ecCodewordsPerBlock), 0);
}

