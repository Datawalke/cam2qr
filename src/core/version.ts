function parseRow(row: EcRow): EcBlocks {
  const groups: EcBlockGroup[] = [{ count: row[1], dataCodewords: row[2] }];
  if (row.length === 5) groups.push({ count: row[3], dataCodewords: row[4] });
  return { ecCodewordsPerBlock: row[0], groups };
}

const VERSIONS: VersionInfo[] = ECC_TABLE.map((levels, i) => ({
  version: i + 1,
  size: sizeForVersion(i + 1),
  alignmentPositions: ALIGNMENT_POSITIONS[i]!,
  ecBlocks: {
    L: parseRow(levels[0]),
    M: parseRow(levels[1]),
    Q: parseRow(levels[2]),
    H: parseRow(levels[3]),
  },
}));

export function getVersionInfo(version: number): VersionInfo {
  const info = VERSIONS[version - 1];
  if (info === undefined) throw new RangeError(`invalid QR version ${version}`);
  return info;
}
